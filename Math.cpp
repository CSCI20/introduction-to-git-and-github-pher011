#include <iostream>

int main() {
    // Variables for the two numbers
    int num1 = 7;
    int num2 = 3;

    // Calculate and output the sum
    std::cout << num1 << " + " << num2 << " = " << num1 + num2 << std::endl;

    // Calculate and output the difference
    std::cout << num1 << " - " << num2 << " = " << num1 - num2 << std::endl;

    // Calculate and output the product
    std::cout << num1 << " * " << num2 << " = " << num1 * num2 << std::endl;

    // Calculate and output the quotient
    std::cout << num1 << " / " << num2 << " = " << num1 / num2 << std::endl;

    // Calculate and output the remainder
    std::cout << num1 << " % " << num2 << " = " << num1 % num2 << std::endl;

    return 0;
}
